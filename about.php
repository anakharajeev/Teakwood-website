                <div class="col-lg-8 inner-paragaph" data-aos="fade-left">
                    <h3>Royal Patronage</h3>
                    <p>Nilambur Teak gained prominence during the reign of the Zamorin Rulers of the Kingdom of Calicut (present-day Kerala). The rulers recognized the superior quality and durability of Nilambur Teak.
                    </p>

                    <h3>Trading Hub</h3>
                    <p>Nilambur, a town in the Malappuram district of Kerala, served as a significant trading hub for teak during the colonial era. The British East India Company established a teak depot in Nilambur in the early 19th century. Teak logs from
                        the nearby forests were transported through the Chaliyar River to the depot and then exported to various parts of the world.
                    </p>

                    <h3>Traditional Architecture</h3>
                    <p>Nilambur Teak has been an integral part of traditional Kerala architecture. It was extensively used in the construction of temples, palaces, and traditional wooden houses. The intricate carvings, ornate pillars, and beautifully crafted
                        doors and windows made from Nilambur Teak showcase the architectural grandeur of the region.
                    </p>

                    <h3>Conservation Efforts</h3>
                    <p>Recognizing the importance of Nilambur Teak, the Kerala Forest Department established the Nilambur Teak Plantation in the early 20th century. The plantation aimed to conserve and sustainably manage teak forests to meet the growing
                        demand for timber. Today, the plantation serves as a research and training center for teak cultivation and conservation. The historic significance of Nilambur Teak lies in its association with the region's cultural heritage, royal
                        patronage, trade, and architectural marvels. It continues to be prized for its quality and durability, keeping the legacy of Nilambur Teak alive in contemporary applications.

                    </p>
